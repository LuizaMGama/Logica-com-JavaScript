/*6) Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a 
área do retângulo.*/

alert("Vamos ler as dimensões do retângulo e calcular sua área");
var valorA = parseInt(prompt("Digite o valor da Base do retângulo: "));
var valorB = parseInt(prompt("Digite o valor da Altura do retângulo: "));
var area = (valorA * valorB); alert("A área do retângulo é: " + area);